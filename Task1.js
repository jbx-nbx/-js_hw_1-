'use strict'; // i am using strict mode as i always prefer.
let i=1,sum=0; // declaring variables(i,sum) : i= starter value(1) , sum=(total value)
while(i<=100){ // condition term : running numbers from 1 to 100(included)
  sum+=i; // adding the value of i to sum .
  i++; // promoting i+=1 every iteration. 
}
console.log("The sum of number from 1 to 100 is:  " +sum); // result : 5050 - checked